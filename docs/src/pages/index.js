import React from 'react';
import classnames from 'classnames';
import Layout from '@theme/Layout';
import Link from '@docusaurus/Link';
import useDocusaurusContext from '@docusaurus/useDocusaurusContext';
import useBaseUrl from '@docusaurus/useBaseUrl';
import styles from './styles.module.css';

const features = [
  {
    title: <>Open Source</>,
    imageUrl: 'img/undraw_docs.svg',
    description: (
      <>
        All of Power+&apos;s code is open source. Versions of Power+ that
        are compiled or minified are distributed with their source maps.
      </>
    ),
  },
  {
    title: <>JSDoc</>,
    imageUrl: 'img/undraw_annotation.svg',
    description: (
      <>
        All of Power+&apos;s source code is annotated with JSDoc syntax.
        Refrence documentation is generated by JSDoc for each release.
      </>
    ),
  },
  {
    title: <>Hosted on GitHub</>,
    imageUrl: 'img/undraw_developer_activity.svg',
    description: (
      <>
        All of Power+&apos;s source code, including this website, is hosted
        on GitHub and is delivered through GitHub pages.
      </>
    ),
  },
];

function Feature({ imageUrl, title, description }) {
  const imgUrl = useBaseUrl(imageUrl);
  return (
    <div className={classnames('col col--4', styles.feature)}>
      {imgUrl && (
        <div className="text--center">
          <img className={styles.featureImage} src={imgUrl} alt={title} />
        </div>
      )}
      <h3>{title}</h3>
      <p>{description}</p>
    </div>
  );
}

function Home() {
  const context = useDocusaurusContext();
  const { siteConfig = {} } = context;
  return (
    <Layout
      title={`Home`}
      description="Documentation homepage">
      <header className={classnames('hero hero--primary', styles.heroBanner)}>
        <div className="container">
          <h1 className="hero__title">{siteConfig.title}</h1>
          <p className="hero__subtitle">{siteConfig.tagline}</p>
          <div className={styles.buttons}>
            <Link
              className={classnames(
                'button button--outline button--secondary button--lg',
                styles.getStarted,
              )}
              to={useBaseUrl('docs/reference')}>
              Read the docs
            </Link>
          </div>
        </div>
      </header>
      <main>
        {features && features.length && (
          <section className={styles.features}>
            <div className="container">
              <div className="row">
                {features.map((props, idx) => (
                  <Feature key={idx} {...props} />
                ))}
              </div>
            </div>
          </section>
        )}
      </main>
    </Layout>
  );
}

export default Home;
